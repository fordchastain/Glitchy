// Deterministic PRNG (mulberry32). Effects that need randomness use a fixed
// seed so their pattern stays stable across redraws — otherwise tweaking any
// unrelated effect would re-roll the noise/displacement underneath it.
export function createRng(seed: number): () => number {
  let a = seed >>> 0;
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
